// Operators

#include <iostream>

using namespace std;

int main() {

    // Declaring 2 decimal variables
    double a, b;
    
    // Input into a and b
    cin >> a >> b;
    
    // Output
    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;

    return 0;
}

