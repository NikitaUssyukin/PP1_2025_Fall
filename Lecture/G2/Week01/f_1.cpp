// Operators

#include <iostream>

using namespace std;

int main() {

    // Declaring 2 integer variables
    int a, b;
    
    // Input into a and b
    cin >> a >> b;
    
    // Output
    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << a % b << endl; // modulus

    return 0;
}

