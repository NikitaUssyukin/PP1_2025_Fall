// Math functions

#include <iostream>
#include <cmath> // contains math functions

using namespace std;

int main() {
    
    int a;
    cin >> a;

    cout << sqrt(a) << endl;
    cout << pow(a, 2) << endl;
    cout << sin(a) << endl;
    cout << abs(a) << endl;
    cout << max(a, 2) << endl;
    cout << min(a, 2) << endl;

    return 0;
}