// Variables

#include <iostream>

using namespace std;

int main() {
    
    int a;
    int b;
    
    cin >> a >> b;

    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << a % b << endl; // modulus - remainder of a divided by b

    return 0;
}