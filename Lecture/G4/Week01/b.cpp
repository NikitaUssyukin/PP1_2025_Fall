// Data types

/*
int - integer numbers (1, 2, 10, 0, -2)
double - decimal numbers (1.23, 0.1, 0.0, -7.7)
float - decimal numbers (1.23, 0.1, 0.0, -7.7)
bool - boolean values (true/false, 1/0)
char - single characters (A, a, $, !, 1)
string - sequence of characters (text)
*/

#include <iostream>

using namespace std;

int main() {

    // output is int
    cout << 2 + 3 << endl;

    // output is double
    cout << 2.23 + 3.67 << endl;

    return 0;
}